const generateWallpapers = () => {
    const result = [];
    let globalId = 20001;

    CATEGORY_MAP.forEach(cat => {
        if (cat.pool && cat.pool.length > 0) {
            cat.pool.forEach((url, index) => {
                let optimizedUrl = url;
                if (url.includes('pexels.com')) {
                    optimizedUrl += optimizedUrl.includes('?') ? '&' : '?';
                    optimizedUrl += 'auto=compress&cs=tinysrgb&w=800';
                }

                result.push({
                    id: globalId++,
                    title: `${cat.name}系列精选`,
                    pic_url: optimizedUrl,
                    score: (8.9 + Math.random() * 1.0).toFixed(1),
                    category_id: cat.id,
                    tags: [cat.name, '美图'],
                    description: `LightScreen 精选壁纸 - ${cat.name}`,
                });
            });
        }
    });

    return result;
};

